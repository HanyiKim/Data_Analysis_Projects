-- 1. Create database and tables for 'Musical Management System'

-- Create database 'Musical'

DROP DATABASE IF EXISTS musical;
CREATE DATABASE IF NOT EXISTS musical;
USE musical;


-- Create entity tables

DROP TABLE IF EXISTS actor;
CREATE TABLE IF NOT EXISTS actor(
 actor_id INT auto_increment primary key,
 first_name VARCHAR(25) NOT NULL,
 last_name VARCHAR(25) NOT NULL,
 contact_no CHAR(13) NOT NULL unique, -- format of (###) #######
 date_of_birth DATE,
 debut_year YEAR,
 cast_history mediumtext);
 
DROP TABLE IF EXISTS shows;
CREATE TABLE IF NOT EXISTS shows(
 show_id int auto_increment primary key,
 title varchar(50) not null unique,
 released_year year not null,
 director varchar(50) not null,
 basic_price double not null
 );
 
DROP TABLE IF EXISTS theater;
CREATE TABLE IF NOT EXISTS theater(
 theater_id int auto_increment primary key,
 location varchar(100) not null,
 contact_no char(13) not null unique);
 
DROP TABLE IF EXISTS show_schedule;
CREATE TABLE IF NOT EXISTS show_schedule(
 schedule_id int auto_increment primary key,
 date_time datetime not null,
 show_id int not null,
 theater_id int not null,
 cast varchar(50),
 
 foreign key(show_id)
  references shows(show_id)
  on delete cascade
  on update cascade,
 foreign key(theater_id)
  references theater(theater_id)
  on delete cascade
  on update cascade
  );
  
DROP TABLE IF EXISTS casting;
CREATE TABLE IF NOT EXISTS casting(
 casting_id int auto_increment primary key,
 actor_id int not null,
 schedule_id int not null,
 
  foreign key(actor_id)
  references actor(actor_id)
  on delete cascade
  on update cascade,
 foreign key(schedule_id)
  references show_schedule(schedule_id)
  on delete cascade
  on update cascade
  );
  
DROP TABLE IF EXISTS ticket;
CREATE TABLE IF NOT EXISTS ticket(
 ticket_id int auto_increment primary key,
 seat_level set('S','A','B','C') not null,
 schedule_id int not null,
 price double, -- price will be calculated based on 'shows.basic_price' and 'seat_level'
 
 foreign key(schedule_id)
  references show_schedule(schedule_id)
  on delete cascade
  on update cascade
  );
  
DROP TABLE IF EXISTS audience;
CREATE TABLE IF NOT EXISTS audience(
 audience_id int auto_increment primary key,
 first_name VARCHAR(25) NOT NULL,
 last_name VARCHAR(25) NOT NULL,
 date_of_birth date not null,
 contact_no CHAR(13) NOT NULL unique,
 preferred_theater int,
 membership_status set('not a member', 'newbie', 'loyal', 'VIP') not null,
 
 foreign key(preferred_theater)
  references theater(theater_id)
  on delete cascade
  on update cascade
  );
  
DROP TABLE IF EXISTS ticket_purchase;
CREATE TABLE IF NOT EXISTS ticket_purchase(
 purchase_id int auto_increment primary key,
 ticket_id int not null unique,
 audience_id int not null,
 final_price double, -- final_price will be calculated based on 'ticket.price' and audiences' attributes
 payment_status set('paid','pending') not null,
 
 foreign key(ticket_id)
  references ticket(ticket_id)
  on delete cascade
  on update cascade,
 foreign key(audience_id)
  references audience(audience_id)
  on delete cascade
  on update cascade
  );
  
  
  
  
  
