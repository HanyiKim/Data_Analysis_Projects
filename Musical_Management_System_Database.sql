-- Casting data
--  3 - 1. Fill data in 'show_schedule.cast' with actors' full names, based on 'casting' table

alter table show_schedule modify cast varchar(500);

update show_schedule join
(select sc.schedule_id, group_concat(concat(a.first_name, ' ', a.last_name) separator ', ') as cast
from actor a right join casting c on a.actor_id = c.actor_id
left join show_schedule sc on c.schedule_id = sc.schedule_id
group by sc.schedule_id) update_cast
on show_schedule.schedule_id = update_cast.schedule_id

set show_schedule.cast = update_cast.cast
where show_schedule.cast is null;


--  3 - 2. Fill data in 'actor.cast_history' with show titles, based on 'casting' table

update actor join
(select a.actor_id, group_concat(title separator ', ') as cast_history
from actor a right join casting c on a.actor_id = c.actor_id
left join show_schedule sc on c.schedule_id = sc.schedule_id
left join shows s on sc.show_id = s.show_id
group by a.actor_id) update_cast_history
on actor.actor_id = update_cast_history.actor_id

set actor.cast_history = update_cast_history.cast_history
where actor.cast_history is null;



-- Price data
-- 3 - 3. Calculate the ticket price with the basis below and update 'ticket' table

/*
ticket price = basic price + 'seat price'

seat price:
seat level C: 0
seat level B: 20
seat level A: 40
seat level S: 80
*/


update ticket join
(select t.ticket_id as ticket_id, sh.basic_price + 
 case
  when t.seat_level = 'C' then 0
  when t.seat_level = 'B' then 20
  when t.seat_level = 'A' then 40
  when t.seat_level = 'S' then 80
 end
as price
from ticket t left join show_schedule sc on t.schedule_id = sc.schedule_id
left join shows sh on sc.show_id = sh.show_id) update_price
on ticket.ticket_id = update_price.ticket_id

set ticket.price = update_price.price
where ticket.price is null
;



-- 3 - 4. Calculate the final price for each audience with the basis below and update 'ticket_purchase' table

/*
final price = ticket price * ('member discount rate') * ('senior or youth discount rate') * (5% discount if preferred_theater)

member discount rate:
not a member: NA
newbie: 10% discount
loyal: 20% discount
VIP: 30% discount

senior or youth discount rate:
60 or older: 20% discount
15 or younger: 20% discount
others: NA

*/

update ticket_purchase join
(select tp.purchase_id as purchase_id, round(t.price *
case
 when a.membership_status = 'not a member' then 1
 when a.membership_status = 'newbie' then 0.9
 when a.membership_status = 'loyal' then 0.8
 when a.membership_status = 'VIP' then 0.7
end
*
case
 when timestampdiff(year, date_of_birth, curdate()) >= 60 or timestampdiff(year, date_of_birth, curdate()) <= 15 then 0.8
 else 1
end
*
case
 when theater_id = preferred_theater then 0.95
 else 1
end
, 1)
as final_price
from ticket t left join ticket_purchase tp on t.ticket_id = tp.ticket_id
left join audience a on tp.audience_id = a.audience_id
left join show_schedule sc on t.schedule_id = sc.schedule_id) update_final_price
on ticket_purchase.purchase_id = update_final_price.purchase_id

set ticket_purchase.final_price = update_final_price.final_price
where ticket_purchase.final_price is null;




-- 4. Answer questions with data aggregation

--  4 - 1. What is the average age of audiences per each membership status?

select membership_status, avg(timestampdiff(year, date_of_birth, curdate())) as average_age
from audience
group by membership_status
order by field(membership_status, 'VIP', 'loyal', 'newbie', 'not a member');


--  4 - 2. How much is the total ticket revenue for shows played from 2023-11-01 to 2023-11-15?

select round(sum(final_price),1) as total_ticket_revenue
from ticket_purchase tp left join ticket t on tp.ticket_id = t.ticket_id
left join show_schedule sc on t.schedule_id = sc.schedule_id
where date(sc.date_time) >= '2023-11-01' and date(sc.date_time) <= '2023-11-15';


--  4 - 3. Which show is the most popular among audiences in their age of 20s?

select title, count(purchase_id) as number_of_audiences
from shows s right join show_schedule sc on s.show_id = sc.show_id
right join ticket t on sc.schedule_id = t.schedule_id
join ticket_purchase tp on t.ticket_id = tp.ticket_id
left join audience a on tp.audience_id = a.audience_id
where timestampdiff(year, date_of_birth, curdate()) >= 20 and timestampdiff(year, date_of_birth, curdate()) < 30
group by title
order by number_of_audiences desc
limit 1;
